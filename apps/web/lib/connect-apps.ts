export interface ConnectApp {
  slug: string;                 // url-safe id; matches Nango provider config
  name: string;                 // display name
  category: ConnectCategory;
  status: 'live' | 'beta' | 'soon';
  tagline: string;              // ≤ 60 chars — shown on card
  popular?: boolean;            // surface in "Most popular" rail
  toolCount?: number;           // approx tools exposed once connected
  scopes?: string;              // human-readable scope hint shown pre-OAuth
  color?: string;               // tailwind class for accent stripe
}

// Compact builder — keeps the file small. Each tuple becomes a full ConnectApp.
type T = [slug: string, name: string, tagline: string, opts?: Partial<Omit<ConnectApp, 'slug' | 'name' | 'tagline' | 'category'>>];

function group(category: ConnectCategory, items: T[]): ConnectApp[] {
  return items.map(([slug, name, tagline, opts]) => ({
    slug, name, tagline, category,
    status: 'soon',
    ...opts,
  }));
}

