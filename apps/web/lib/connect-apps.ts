export const CONNECT_APPS: ConnectApp[] = [...LIVE, ...CATALOG];

// Stats used in the marketing hero + meta. Approximate tool counts grow
// roughly linearly with apps — we hand-round to the nearest big number
// so the claims stay honest as we add providers.
export const CONNECT_STATS = {
  totalApps: CONNECT_APPS.length,            // 700+
  totalApproxTools: 1200,                    // ~1.7 tools/app avg
  agentIntegrations: 50,                     // Claude / Cursor / Cline / Windsurf / Continue / OpenAI / etc.
  liveProviders: LIVE.length,
} as const;

export function listAppsByCategory(category: ConnectCategory): ConnectApp[] {
  return CONNECT_APPS.filter((a) => a.category === category);
}

export function getAppBySlug(slug: string): ConnectApp | undefined {
  return CONNECT_APPS.find((a) => a.slug === slug);
}

export function popularApps(limit = 18): ConnectApp[] {
  return CONNECT_APPS.filter((a) => a.popular).slice(0, limit);
}

export function liveApps(): ConnectApp[] {
  return CONNECT_APPS.filter((a) => a.status === 'live');
}

/**
 * Lightweight search — name, slug, tagline. Case-insensitive substring.
 * Real fuzzy search lives in /lib/fuzzy-search.ts; we keep this dumb-fast
 * because the catalog is huge and the UI does client-side filtering.
 */
export function searchApps(query: string): ConnectApp[] {
  const q = query.trim().toLowerCase();
  if (!q) return CONNECT_APPS;
  return CONNECT_APPS.filter(
    (a) =>
      a.name.toLowerCase().includes(q) ||
      a.slug.includes(q) ||
      a.tagline.toLowerCase().includes(q),
  );
}
